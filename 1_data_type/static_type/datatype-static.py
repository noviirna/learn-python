# This is an example on how to assign variable with example value of related datatype

## 1. Integer, represent integers
integerVar = 2021
integerVar2 = -2021
print("\n1. Integer")
print(type(integerVar)) # output: <class 'int'>
print(type(integerVar2)) # output: <class 'int'>

## 2. Float, represent decimals
floatVar = 1.21
floatVar2 = -1.21
print("\n2.Float")
print(type(floatVar)) # output: <class 'float'>
print(type(floatVar2)) # output: <class 'float'>

## 3. String, represent text
stringVar = '2021'
stringVar2 = 'Python123'
print("\n3.String")
print(type(stringVar)) # output: <class 'str'>
print(type(stringVar)) # output: <class 'str'>

## 4. Boolean, represent true / false
booleanVar = True
booleanVar2 = False
print("\n4.Boolean")
print(type(booleanVar)) # output: <class 'bool'>
print(type(booleanVar2)) # output: <class 'bool'>

## 5. Complex, represent imaginary value
complexVar = 1 + 1j
complexVar2 = 1j
print("\n5.Complex")
print(type(complexVar)) # output: <class 'complex'>
print(type(complexVar2)) # output: <class 'complex'>
